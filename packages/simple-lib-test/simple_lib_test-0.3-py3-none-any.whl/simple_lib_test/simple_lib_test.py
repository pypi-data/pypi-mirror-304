def fetch_data(code):
    if code == 1:
        return '1'
    elif code == 2:
        return '2'
    elif code == 3:
        return '3'
    elif code == 4:
        return '4'
    elif code == 5:
        return '5'
    elif code == 6:
        return '6'
    else:
        return 'null'
