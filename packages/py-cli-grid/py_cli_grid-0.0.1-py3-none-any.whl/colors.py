class Color:
    """New class to manage TRUE COLOR in terminals"""

    @staticmethod
    def rgb_to_color_scape(r, g, b, background=False):
        """Transforms RGB code to terminal scape color"""
        return '\033[{};2;{};{};{}m'.format(48 if background else 38, r, g, b)
    
